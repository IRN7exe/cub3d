#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char	*t;
	size_t	i;

	t = s;
	i = 0;
	while (i < n)
	{
		t[i] = (char)c;
		i++;
	}
	return (s);
}
